package com.f.diary.friends;

public class FriendsVO {
	private int f_num;
	private int f_m_id;
	private String f_group;
	private int f_friendsid;
	
	public int getF_num() {
		return f_num;
	}
	public void setF_num(int f_num) {
		this.f_num = f_num;
	}
	public int getF_m_id() {
		return f_m_id;
	}
	public void setF_m_id(int f_m_id) {
		this.f_m_id = f_m_id;
	}
	public String getF_group() {
		return f_group;
	}
	public void setF_group(String f_group) {
		this.f_group = f_group;
	}
	public int getF_friendsid() {
		return f_friendsid;
	}
	public void setF_friendsid(int f_friendsid) {
		this.f_friendsid = f_friendsid;
	}
	
	
}
